    private void jBexcluirUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBexcluirUsuarioActionPerformed
        int linha = jTblUsuario.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Você deve selecionar um usuário.");
        } else {
            int codigo = (int) jTblUsuario.getValueAt(linha, 0);//recebe linha e coluna, e converte para inteiro
            if (controllerUsuario.excluirUsuarioController(codigo)) {
                carregarUsuarios();
                limparFormulário();
                JOptionPane.showMessageDialog(this, "Usuário Excluído com Sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao Excuir Usuário!");
            }

        }
    }//GEN-LAST:event_jBexcluirUsuarioActionPerformed

    private void jBalterarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBalterarUsuarioActionPerformed
        //editar usuário através do ID retornado do banco.
        cadAlt = "alt";
        modelUsuario = new ModelUsuario();
        int linha = jTblUsuario.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Você deve selecionar um usuário.");
        } else {
            int codigo = (int) jTblUsuario.getValueAt(linha, 0);//recebe linha e coluna, e converte para inteiro
            modelUsuario = controllerUsuario.getUsuarioController(codigo);
            jTfCodigoUsuario.setText(String.valueOf(modelUsuario.getUsuId()));
            jTfNomeUsuario.setText(modelUsuario.getUsuNome());
            jTfLogin.setText(modelUsuario.getUsuLogin());
            jPfSenhaUsuario.setText(modelUsuario.getUsuSenha());
        }
    }//GEN-LAST:event_jBalterarUsuarioActionPerformed

    //Limpar formulário de usuário
    private void limparFormulário() {//limpar formulálior usuário, private pois so essa classe usará o método.
        jTfCodigoUsuario.setText("");
        jTfNomeUsuario.setText("");
        jTfLogin.setText("");
        jPfSenhaUsuario.setText("");
        cadAlt = "cad";
    }

    /**
     * Carrega uma lista de usuários na tabela.
     */
    private void carregarUsuarios() {
        listUsuarios = controllerUsuario.getListaUsuariosController();//preencher a lista
        DefaultTableModel modelo = (DefaultTableModel) jTblUsuario.getModel();//instacia uma tabela básica e recebe a tabela do sistema.
        modelo.setRowCount(0);//limpar a tabela.

        //preencher tabela
        for (int i = 0; i < listUsuarios.size(); i++) {
            modelo.addRow(new Object[]{
                listUsuarios.get(i).getUsuId(), //posição do i igual a 0
                listUsuarios.get(i).getUsuNome(), //posição do i igual a 0
                listUsuarios.get(i).getUsuLogin() //posição do i igual a 0
            });

        }
    }

