    private void jBsalvarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBsalvarUsuarioActionPerformed
        modelUsuario = new ModelUsuario();

        if (jTfNomeUsuario.getText() == null || jTfNomeUsuario.getText().trim().equals("") || jTfLogin.getText() == null || jTfLogin.getText().trim().equals("")
                || jPfSenhaUsuario.getPassword() == null || jPfSenhaUsuario.getPassword().length == 0) {
            JOptionPane.showMessageDialog(this, "Preenchimento dos campos obrigatórios!", "Atençao!", JOptionPane.WARNING_MESSAGE);
        } else {
            modelUsuario.setUsuNome(jTfNomeUsuario.getText());
            modelUsuario.setUsuLogin(jTfLogin.getText());
            modelUsuario.setUsuSenha(String.valueOf(jPfSenhaUsuario.getPassword()));

            if (cadAlt.equals("cad")) {
                if (controllerUsuario.salvarUsuarioController(modelUsuario)) {
                    //Cadastrou
                    JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                    //Limpar
                    limparFormulário();
                    carregarUsuarios();
                } else {
                    //não cadastrou
                    JOptionPane.showMessageDialog(this, "Erro ao cadastrar!", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                modelUsuario.setUsuId(Integer.parseInt(jTfCodigoUsuario.getText()));
                if (controllerUsuario.atualizarUsuarioController(modelUsuario)) {
                    //Alterou
                    JOptionPane.showMessageDialog(this, "Usário atualizado com sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                    //Limpar
                    limparFormulário();
                    carregarUsuarios();
                } else {
                    //Não alterou
                    JOptionPane.showMessageDialog(this, "Erro ao atualizar usuário!", "Erro", JOptionPane.ERROR_MESSAGE);
                }

            }

        }

    }//GEN-LAST:event_jBsalvarUsuarioActionPerformed

